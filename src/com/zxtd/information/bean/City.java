package com.zxtd.information.bean;

public class City {

	private int cityId;
	private String cityName;
	private String cityKey;
	private int districtId;
	private int sort;
	private boolean isHot;
	private String pyCityName;
	private String pyShort;
	public int getCityId() {
		return cityId;
	}
	public void setCityId(int cityId) {
		this.cityId = cityId;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getCityKey() {
		return cityKey;
	}
	public void setCityKey(String cityKey) {
		this.cityKey = cityKey;
	}
	public int getDistrictId() {
		return districtId;
	}
	public void setDistrictId(int districtId) {
		this.districtId = districtId;
	}
	public int getSort() {
		return sort;
	}
	public void setSort(int sort) {
		this.sort = sort;
	}
	public boolean isHot() {
		return isHot;
	}
	public void setHot(boolean isHot) {
		this.isHot = isHot;
	}
	public String getPyCityName() {
		return pyCityName;
	}
	public void setPyCityName(String pyCityName) {
		this.pyCityName = pyCityName;
	}
	public String getPyShort() {
		return pyShort;
	}
	public void setPyShort(String pyShort) {
		this.pyShort = pyShort;
	}
	
	
	
}
